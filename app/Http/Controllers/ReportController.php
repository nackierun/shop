    public function confirmReport(Request $request)
    {
        \App\Report::create([
            'customer_id'    => Auth::user()->id,
            'orderdetail_id' => $request->orderdetail_id,
            'image'          => $request->file('image')->store('images', 'public'),
        ]);
        return redirect('customers/orders/. Auth::user()->id )');
    }

